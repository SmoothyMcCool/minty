package tom.tasks.emit.document;

public class FileData {

	private String file;
	private String name;

	public FileData() {
		file = "";
		name = "";
	}

	public String getFile() {
		return file;
	}

	public void setFile(String file) {
		this.file = file;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}
